/*
* 变量的命名规则：
* 变量名=首字母（1.字母\ 2.下划线“_”\ 3.'$'符号）+其余部分（任意数量的）
* */
public class ZiYouXia {
    public static void main(String[] args) {

        //昵称
        String name = "北大青鸟";

        //性别
        //单字符类型 引号市单引号
        char sex = '男';

        //年龄
        int age = 20;

        //是否会员 true 真  false 假
        boolean isMember = false;

        System.out.println("昵称："+name);
        System.out.println("性别："+sex);
        System.out.println("年龄："+age);
    }
}
